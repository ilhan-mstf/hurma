package com.cennetelmasi.hurma.server;

public class NodeTypeObject {
	private int id;
	private String name;
	private String MIB;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getMIB() {
		return MIB;
	}
	
	public void setMIB(String mIB) {
		MIB = mIB;
	}

}
